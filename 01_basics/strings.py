
# variable storing string value
string = "Hello World"

# print string
print("hello world")
# print using variable
print("print using variable: " + string)

# divide the world in different lines using '\n'
print("hello\nworld")

#add two strings 
print("hello " + "world")

# convert the whole string to lowercase
print(string.lower())
# convert the whole string to uppercase
print(string.upper())
# check whether the string is uppercase
print(string.isupper())
# check whether the string is lowercase
print(string.islower())

# find the lenght of the string
print(len(string))

# get the character of the element at the 0 index of the string
print(string[0])
# return the index of the letter passed 'W'
print(string.index('W'))
# return the index of given phrase 'Wor'
print(string.index("Wor"))

# replace the existing string with the new given string
print(string.replace("World", "Universe"))



